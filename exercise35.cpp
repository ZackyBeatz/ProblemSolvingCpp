#include <iostream>
using namespace std;

int main(int argc, const char * argv[]) {
    
    int x = 0;
    
    if (x < 0) {
        x = 7; cout << "x is now positive.";}
    
    else
    {
        x = - 7; cout << "x is now negative.";
    }
    
    return 0;
}
